void	ft_sort_int_tab(int *tab, int size)
{
	int	x;
	int	y;
	int	z;

	y = 0;
	while (y <= (size))
	{
		x = y + 1;
		while (x <= (size))
		{
			if (tab[x] <= tab[y])
			{
				z = tab[x];
				tab[x] = tab[y];
				tab[y] = z;
			}
			x++;
		}
		y++;
	}
}
