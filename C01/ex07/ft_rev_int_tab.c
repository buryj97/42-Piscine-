void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	d;
	int	s;

	i = 0;
	d = size - 1;
	while (i < (size / 2))
	{
		s = tab[i];
		tab[i] = tab[d];
		tab[d] = s;
		i++;
		d--;
	}
}
