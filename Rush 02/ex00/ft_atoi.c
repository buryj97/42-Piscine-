int     ft_atoi(char *str)
{
        int     n;
        int     i;
        int     sign;

        i = 0;
        while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
                i++;
        sign = 1;
        while (str[i] == '-' || str[i] == '+')
        {
                if (str[i] == '-')
                        sign = -sign;
                i++;
        }
        n = 0;
        while (str[i] >= '0' && str[i] <= '9')
                n = n * 10 + str[i++] - '0';
        return (n * sign);
}
