int	ft_iterative_factorial(int nb)
{
	int	fact;
	int	i;

	if (nb < 0)
		return (0);
	fact = 1;
	i = 1;
	while (i <= nb)
	{
		fact = fact * i;
		i++;
	}
	return (fact);
}
