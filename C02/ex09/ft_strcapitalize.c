char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] += 32;
		}
		i++;
	}
	while (str[i] >= 'a' && str[i] <= 'z')
	{
		if (!(str[i - 1] >= 'a' && str[i - 1] <= 'z'))
		{
			str[i] -= 32;
		}
		i++;
	}
	return (str);
}
